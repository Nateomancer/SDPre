public class Objective7Lab1 {
  public static void main(String[] args) {

    //Declare counter variable for loop
    int counter = 10;


    //Create  while loop
    while(counter > 0) {


      //Outputs current counter int
      System.out.println(counter);


      //At the end of the while loop, counter will be decreaed by 1
      counter = counter - 1;

    }//End of loop after this bracket

  }
}
